/**
 * Function to reverse an array using built-in method.
 *
 * Time Complexity:
 *  - O(n), the reverse method has linear time complexity.
 *
 * Auxiliary Space:
 *  - O(1) Additional space is not used to store the reversed array, as the in-built array method swaps the values in-place.
 *
 */

export function reverseArrayBuiltInMethod(arr) {
  arr.reverse()
}
