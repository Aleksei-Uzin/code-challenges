function getName(f, arg) {
  return arg ? `${f.name} ${arg}` : `${f.name}.`
}
function Adam(arg) {
  return getName(Adam, arg)
}
function has(arg) {
  return getName(has, arg)
}
function a(arg) {
  return getName(a, arg)
}
function dog(arg) {
  return getName(dog, arg)
}
function The(arg) {
  return getName(The, arg)
}
function name(arg) {
  return getName(name, arg)
}
function of(arg) {
  return getName(of, arg)
}
function the(arg) {
  return getName(the, arg)
}
function is(arg) {
  return getName(is, arg)
}
function also(arg) {
  return getName(also, arg)
}
