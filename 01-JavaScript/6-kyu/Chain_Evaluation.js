Number.prototype.add = function (n) {
  return this + n
}
Number.prototype.subtract = function (n) {
  return this - n
}
Number.prototype.multiply = function (n) {
  return this * n
}
Number.prototype.divide = function (n) {
  return this / n
}
Number.prototype.square = function () {
  return this ** 2
}
